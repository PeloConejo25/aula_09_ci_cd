def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def sum_of_numbers(number1, number2):
    sum = number1 + number2
    return sum

def sub_of_numbers(number1, number2):
    sub = number1 - number2
    return sub

def multiply_numbers(number1, number2):
    mult = number1 * number2
    return mult

def divide_numbers(number1, number2):
    divis = number1 / number2
    return divis